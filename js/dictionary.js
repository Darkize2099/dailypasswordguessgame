/**
 * Gets a consistent password based on the current date string
 * This ensures the same password for everyone worldwide today.
 */
function getDailyPassword() {
    const today = new Date();
    // Create a unique string for the day: e.g., "2024-05-22"
    const dateKey = `${today.getFullYear()}-${today.getMonth() + 1}-${today.getDate()}`;
    
    // Simple hash function to turn string into a number
    let hash = 0;
    for (let i = 0; i < dateKey.length; i++) {
        hash = ((hash << 5) - hash) + dateKey.charCodeAt(i);
        hash |= 0; // Convert to 32bit integer
    }
    
    // Use the absolute hash value to pick the index
    const index = Math.abs(hash) % PASSWORDS.length;
    return PASSWORDS[index].toUpperCase();
}